class MexicanEconomySimulator:
    def __init__(self, n_clientes=10000):
        self.n_clientes = n_clientes
        np.random.seed(2026)

    def generate_datasets(self, path):
        ids = np.arange(100000, 100000 + self.n_clientes)
        meses = np.random.randint(1, 13, self.n_clientes)
        flag_informal = np.random.binomial(1, 0.55, self.n_clientes)
        ingreso = np.random.normal(20000, 5000, self.n_clientes)

        # AJUSTE: Creamos correlación real para que el modelo tenga qué aprender
        # Probabilidad de impago = Base + Penalización por Informalidad - Crédito al Ingreso
        prob_base = 0.05 + (0.25 * flag_informal) + (0.10 * (ingreso < 15000))
        prob_base = np.clip(prob_base, 0, 1)
        target = np.random.binomial(1, prob_base)

        df_app = pd.DataFrame({
            'ID_CLIENTE': ids,
            'MES_SOLICITUD': meses,
            'FLAG_INFORMAL': flag_informal,
            'INGRESO_REAL_AJUSTADO_MXN': ingreso,
            'MONTO_CREDITO_MXN': np.random.uniform(10000, 100000, self.n_clientes),
            'TARGET': target
        })

        if not os.path.exists(path): os.makedirs(path)
        df_app.to_csv(f"{path}solicitudes_train.csv", index=False)
        print("✅ Datos generados con SEÑAL DE RIESGO (Informalidad -> Default).")

